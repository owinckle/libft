#include "libft.h"

void	first_word(char *str, int i, int o)
{
	while (str[i])
	{
		while (str[i] != ' ' && str[i] != '\0')
		{
			o = 42;
			ft_putchar(str[i]);
			i++;
		}
		if (o == 42)
			return ;
		i++;
	}
}
